#include <iostream>
#include <iomanip>
using namespace std;

int main() {
    // Матрица A — количество проданных товаров (3 продавца × 4 товара)
    double A[3][4] = {
            {5, 2, 0, 10},
            {3, 5, 2, 5},
            {20, 0, 0, 4}
    };

    // Матрица B — цены и комиссионные (4 товара × 2 показателя)
    double B[4][2] = {
            {1.20, 0.50},
            {2.80, 0.40},
            {5.00, 1.00},
            {2.00, 1.50}
    };

    // Матрица C = A × B (3 продавца × 2 показателя)
    double C[3][2] = {0};

    // Вычисляем произведение матриц
    for (int i = 0; i < 3; ++i) {
        for (int j = 0; j < 2; ++j) {
            for (int k = 0; k < 4; ++k) {
                C[i][j] += A[i][k] * B[k][j];
            }
        }
    }

    cout << fixed << setprecision(2);

    cout << "Матрица C (доход и комиссионные):\n";
    for (int i = 0; i < 3; ++i) {
        cout << "Продавец " << i + 1 << ": ";
        cout << "Выручка = " << C[i][0] << " , Комиссионные = " << C[i][1] << endl;
    }

    // 1) Определяем, кто выручил больше и меньше
    int maxRev = 0, minRev = 0;
    for (int i = 1; i < 3; ++i) {
        if (C[i][0] > C[maxRev][0]) maxRev = i;
        if (C[i][0] < C[minRev][0]) minRev = i;
    }

    cout << "\n1) Больше всего выручил продавец " << maxRev + 1
         << ", меньше всего — продавец " << minRev + 1 << endl;

    // 2) Кто получил больше/меньше комиссионных
    int maxCom = 0, minCom = 0;
    for (int i = 1; i < 3; ++i) {
        if (C[i][1] > C[maxCom][1]) maxCom = i;
        if (C[i][1] < C[minCom][1]) minCom = i;
    }

    cout << "2) Наибольшие комиссионные у продавца " << maxCom + 1
         << ", наименьшие — у продавца " << minCom + 1 << endl;

    // 3) Общая сумма денег, вырученных за проданные товары
    double totalSales = C[0][0] + C[1][0] + C[2][0];
    cout << "3) Общая сумма выручки: " << totalSales << endl;

    // 4) Общие комиссионные
    double totalCom = C[0][1] + C[1][1] + C[2][1];
    cout << "4) Всего комиссионных: " << totalCom << endl;

    // 5) Общая сумма денег, прошедших через руки продавцов
    cout << "5) Общая сумма (выручка + комиссионные): " << totalSales + totalCom << endl;

    return 0;
}
