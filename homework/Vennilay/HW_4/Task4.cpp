#include <iostream>

int main() {
    // Вариант I: m = 37, i = 3, c = 64
    int m1 = 37, i1 = 3, c1 = 64;

    // Вариант II: m = 25173, i = 13849, c = 65537
    int m2 = 25173, i2 = 13849, c2 = 65537;

    int n;
    std::cout << "Сколько чисел генерировать? ";
    std::cin >> n;

    auto s1 = new int[n];
    auto s2 = new int[n];
    s1[0] = 0;
    s2[0] = 0;

    std::cout << "\n════════════════════════════════════════\n";
    std::cout << "Вариант I (m=37, i=3, c=64)\n";
    std::cout << "════════════════════════════════════════\n";

    std::cout << "0) " << s1[0] << "\n";

    for (int i = 0; i < n - 1; i++) {
        s1[i + 1] = (m1 * s1[i] + i1) % c1;
        std::cout << (i+1) << ") " << s1[i+1] << "\n";
        std::cout << "   Вычисление: (" << m1 << " * " << s1[i] << " + " << i1 << ") % " << c1
                  << " = (" << (m1 * s1[i]) << " + " << i1 << ") % " << c1
                  << " = " << (m1 * s1[i] + i1) << " % " << c1 << " = " << s1[i+1] << "\n";
    }

    std::cout << "\n════════════════════════════════════════\n";
    std::cout << "Вариант II (m=25173, i=13849, c=65537)\n";
    std::cout << "════════════════════════════════════════\n";

    std::cout << "0) " << s2[0] << "\n";

    for (int i = 0; i < n - 1; i++) {
        s2[i + 1] = (m2 * s2[i] + i2) % c2;
        std::cout << (i+1) << ") " << s2[i+1] << "\n";
        std::cout << "   Вычисление: (" << m2 << " * " << s2[i] << " + " << i2 << ") % " << c2
                  << " = (" << (m2 * s2[i]) << " + " << i2 << ") % " << c2
                  << " = " << (m2 * s2[i] + i2) << " % " << c2 << " = " << s2[i+1] << "\n";
    }

    delete[] s1;
    delete[] s2;
    return 0;
}
