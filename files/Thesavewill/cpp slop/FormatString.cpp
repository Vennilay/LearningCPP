#include <iostream>
#include <cmath>
#include <windows.h>
using namespace std;
int main()
{
    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);

    //Можно запустить и увидеть как всё будет (Я думаю только еблан это не поймёт)

    // Сброс форматирования
    cout << "\033[0m" << "Сброс форматирования (по умолчанию)" << endl;

    // --- Стили текста ---
    cout << "\033[1m" << "Жирный текст" << "\033[0m" << endl;
    cout << "\033[2m" << "Тусклый текст" << "\033[0m" << endl;
    cout << "\033[3m" << "Курсив" << "\033[0m" << endl;
    cout << "\033[4m" << "Подчёркнутый" << "\033[0m" << endl;
    cout << "\033[5m" << "Мигающий" << "\033[0m" << endl;
    cout << "\033[7m" << "Инвертированные цвета" << "\033[0m" << endl;
    cout << "\033[9m" << "Зачёркнутый текст" << "\033[0m" << endl;

    // --- Цвет текста (Foreground) ---
    cout << "\033[30m" << "Чёрный" << "\033[0m" << endl;
    cout << "\033[31m" << "Красный" << "\033[0m" << endl;
    cout << "\033[32m" << "Зелёный" << "\033[0m" << endl;
    cout << "\033[33m" << "Жёлтый" << "\033[0m" << endl;
    cout << "\033[34m" << "Синий" << "\033[0m" << endl;
    cout << "\033[35m" << "Пурпурный" << "\033[0m" << endl;
    cout << "\033[36m" << "Голубой" << "\033[0m" << endl;
    cout << "\033[37m" << "Белый (светло-серый)" << "\033[0m" << endl;

    // --- Цвет фона (Background) ---
    cout << "\033[40m\033[37m" << "Чёрный фон" << "\033[0m" << endl;
    cout << "\033[41m\033[30m" << "Красный фон" << "\033[0m" << endl;
    cout << "\033[42m\033[30m" << "Зелёный фон" << "\033[0m" << endl;
    cout << "\033[43m\033[30m" << "Жёлтый фон" << "\033[0m" << endl;
    cout << "\033[44m\033[37m" << "Синий фон" << "\033[0m" << endl;
    cout << "\033[45m\033[37m" << "Пурпурный фон" << "\033[0m" << endl;
    cout << "\033[46m\033[30m" << "Голубой фон" << "\033[0m" << endl;
    cout << "\033[47m\033[30m" << "Белый фон" << "\033[0m" << endl;

    // --- Яркие цвета ---
    cout << "\033[90m" << "Ярко-чёрный (серый)" << "\033[0m" << endl;
    cout << "\033[91m" << "Ярко-красный" << "\033[0m" << endl;
    cout << "\033[92m" << "Ярко-зелёный" << "\033[0m" << endl;
    cout << "\033[93m" << "Ярко-жёлтый" << "\033[0m" << endl;
    cout << "\033[94m" << "Ярко-синий" << "\033[0m" << endl;
    cout << "\033[95m" << "Ярко-пурпурный" << "\033[0m" << endl;
    cout << "\033[96m" << "Ярко-голубой" << "\033[0m" << endl;
    cout << "\033[97m" << "Ярко-белый" << "\033[0m" << endl;

    // --- Комбинированные стили ---
    cout << "\033[1;31m" << "Жирный красный" << "\033[0m" << endl;
    cout << "\033[4;34m" << "Синий и подчёркнутый" << "\033[0m" << endl;
    cout << "\033[3;33;44m" << "Курсив, жёлтый текст на синем фоне" << "\033[0m" << endl;

    // --- 256 цветов (пример) ---
    for (int i = 0; i < 16; ++i) {
        cout << "\033[48;5;" << i << "m" << "  " << "\033[0m";
    }
    cout << endl;

    // --- RGB цвета (пример) ---
    cout << "\033[38;2;255;0;0m" << "Красный (RGB)" << "\033[0m" << endl;
    cout << "\033[38;2;0;255;0m" << "Зелёный (RGB)" << "\033[0m" << endl;
    cout << "\033[38;2;0;0;255m" << "Синий (RGB)" << "\033[0m" << endl;

    return 0;
}
